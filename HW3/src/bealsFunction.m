function [ y, gy, hy ] = bealsFunction( x )
    t = num2cell(x);
    [v1, v2] = t{:};
    
    y = (1.5 - v1 + v1*v2)^2 + (2.25 - v1 + v1*v2^2)^2 + ...
        (2.625 - v1 + v1*v2^3)^2; 
    gy = [2*(v2^2 - 1)*(v1*v2^2 - v1 + 9/4)...
        + 2*(v2^3 - 1)*(v1*v2^3 - v1 + 21/8)...
        + 2*(v2 - 1)*(v1*v2 - v1 + 3/2);
        2*v1*(v1*v2 - v1 + 3/2) + 4*v1*v2*(v1*v2^2 - v1 + 9/4)...
        + 6*v1*v2^2*(v1*v2^3 - v1 + 21/8)];
    hy = [2*(v2 - 1)^2 + 2*(v2^2 - 1)^2 + 2*(v2^3 - 1)^2,...
        6*v2^2*(v1*v2^3 - v1 + 21/8) - 2*v1 + 2*v1*v2...
        + 2*v1*(v2 - 1) + 4*v2*(v1*v2^2 - v1 + 9/4)...
        + 4*v1*v2*(v2^2 - 1) + 6*v1*v2^2*(v2^3 - 1) + 3;...
        6*v2^2*(v1*v2^3 - v1 + 21/8) - 2*v1 + 2*v1*v2...
        + 2*v1*(v2 - 1) + 4*v2*(v1*v2^2 - v1 + 9/4)...
        + 4*v1*v2*(v2^2 - 1) + 6*v1*v2^2*(v2^3 - 1) + 3,...
        8*v1^2*v2^2 + 18*v1^2*v2^4 + 4*v1*(v1*v2^2 - v1 + 9/4)...
        + 2*v1^2 + 12*v1*v2*(v1*v2^3 - v1 + 21/8)];
    
end

