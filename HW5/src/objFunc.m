function [ r, y, Jr, gy] = objFunc( x1, x2 )
    r = [34/5 - x1*exp(x2/2), 3 - x1*exp(x2), ...
        3/2 - x1*exp((3*x2)/2), 3/4 - x1*exp(2*x2), ...
        12/25 - x1*exp((5*x2)/2), 1/4 - x1*exp(3*x2), ...
        1/5 - x1*exp((7*x2)/2), 3/20 - x1*exp(4*x2)]';
    
    y = 1/2*((x1*exp(x2) - 3)^2 + (x1*exp(3*x2) - 1/4)^2 ...
        + (x1*exp(2*x2) - 3/4)^2 + (x1*exp((3*x2)/2) - 3/2)^2 ...
        + (x1*exp((7*x2)/2) - 1/5)^2 + (x1*exp(4*x2) - 3/20)^2 ...
        + (x1*exp(x2/2) - 34/5)^2 + (x1*exp((5*x2)/2) - 12/25)^2);
    
    gy = [exp(2*x2)*(x1*exp(2*x2) - 3/4) + ...
        exp(3*x2)*(x1*exp(3*x2) - 1/4) ...
        + exp((3*x2)/2)*(x1*exp((3*x2)/2) - 3/2) ...
        + exp((7*x2)/2)*(x1*exp((7*x2)/2) - 1/5) ...
        + exp(4*x2)*(x1*exp(4*x2) - 3/20) ...
        + exp(x2/2)*(x1*exp(x2/2) - 34/5) ...
        + exp((5*x2)/2)*(x1*exp((5*x2)/2) - 12/25) ...
        + exp(x2)*(x1*exp(x2) - 3); ...
        2*x1*exp(2*x2)*(x1*exp(2*x2) - 3/4) ...
        + 3*x1*exp(3*x2)*(x1*exp(3*x2) - 1/4) ...
        + (3*x1*exp((3*x2)/2)*(x1*exp((3*x2)/2) - 3/2))/2 ...
        + (7*x1*exp((7*x2)/2)*(x1*exp((7*x2)/2) - 1/5))/2 ...
        + 4*x1*exp(4*x2)*(x1*exp(4*x2) - 3/20) ...
        + (x1*exp(x2/2)*(x1*exp(x2/2) - 34/5))/2 ...
        + (5*x1*exp((5*x2)/2)*(x1*exp((5*x2)/2) - 12/25))/2 ...
        + x1*exp(x2)*(x1*exp(x2) - 3)];
    
    Jr = [-exp(conj(x2)/2),       -(exp(conj(x2)/2)*conj(x1))/2;
        -exp(conj(x2)),             -exp(conj(x2))*conj(x1);
        -exp((3*conj(x2))/2), -(3*exp((3*conj(x2))/2)*conj(x1))/2;
        -exp(2*conj(x2)),         -2*exp(2*conj(x2))*conj(x1);
        -exp((5*conj(x2))/2), -(5*exp((5*conj(x2))/2)*conj(x1))/2;
        -exp(3*conj(x2)),         -3*exp(3*conj(x2))*conj(x1);
        -exp((7*conj(x2))/2), -(7*exp((7*conj(x2))/2)*conj(x1))/2;
        -exp(4*conj(x2)),         -4*exp(4*conj(x2))*conj(x1)];
end

